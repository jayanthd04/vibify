const mongoose = require('mongoose');

const dataSchema = new mongoose.Schema({
	track_id:{
		required:true,
		type:String
	},
	artists:{
		required:true,
		type:String
	},
	album_name:{
		required:true,
		type:String
	},
	track_name:{
		required:true,
		type:String
	},
	popularity:{
		required:true,
		type:Number
	},
	duration_ms:{
		required:true,
		type:Number
	},
	explicit:{
		required:true,
		type:Boolean
	},
	danceability:{
		required:true,
		type:Number
	},
	energy:{
		required:true,
		type:Number
	},
	key:{	
		required:true,
		type:Number
	},
	loudness:{
		required:true,
		type:Number
	},
	mode:{
		required:true,
		type:Number
	},
	speechiness:{
		required:true,
		type:Number
	},
	acousticness:{
		required:true,
		type:Number
	},
	instrumentalness:{
		required:true,
		type:Number
	},
	liveness:{
		required:true,
		type:Number
	},
	valence:{
		required:true,
		type:Number
	},
	tempo:{
		required:true,
		type:Number
	},
	time_signature:{
		required:true,
		type:Number
	},
	track_genre:{
		required:true,
		type:String
	}
});

module.exports = mongoose.model('Track', dataSchema);
